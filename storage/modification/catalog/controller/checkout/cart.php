                public function getCustomerId($data){
                  if(isset($data['option'])){
                    foreach ($data['option'] as $key => $value) {

                      $result = $this->db->query("SELECT customer_id FROM ".DB_PREFIX."product_option_value pov LEFT JOIN ".DB_PREFIX."option_mapping om ON (pov.option_value_id = om.option_value_id) WHERE pov.product_option_value_id = '".(int)$value."' AND pov.product_option_id = '".(int)$key."' AND pov.product_id = '".(int)$data['product_id']."' ")->row;

                      return $result;
                    }
                  }
                }

                public function chk($product_id)
                {

                  $json = array();

                  if ($this->config->get('module_marketplace_status')) {
                    $seller = $this->db->query("SELECT customer_id FROM " . DB_PREFIX . "customerpartner_to_product cpp LEFT JOIN " . DB_PREFIX . "product p ON (cpp.product_id = p.product_id) WHERE cpp.product_id = '" . (int)$product_id . "'")->row;
                    if (isset($seller['customer_id']))
                      $seller_id = $seller['customer_id'];
                    else
                      $seller_id = 'Admin';
                    foreach ($this->cart->getProducts() as $key => $value) {
                      $product = $value;
                      $oldProduct_id = $product['product_id'];
                      $oldSeller = $this->db->query("SELECT customer_id FROM " . DB_PREFIX . "customerpartner_to_product cpp LEFT JOIN " . DB_PREFIX . "product p ON (cpp.product_id = p.product_id) WHERE cpp.product_id = '" . (int)$oldProduct_id . "'")->row;

                      if (isset($oldSeller['customer_id'])) {					
                        if ($seller_id != 'Admin' && $oldSeller['customer_id'] != $seller_id) {
                          $json['success'] = true;
                        }

                        if ($seller_id == 'Admin' && $oldSeller['customer_id'] != $seller_id) {
                          $json['success'] = true;
                        }
                      }
                    }
                    if (!$json) {
                      $json['success'] = false;
                    }
                  } // only single seller

                  return $json;
                }
              
	public function add() {
		$this->load->language('checkout/cart');

		$json = array();

		if (isset($this->request->post['product_id'])) {
			$product_id = (int)$this->request->post['product_id'];
		} else {
			$product_id = 0;
		}

		$this->load->model('catalog/product');

		$product_info = $this->model_catalog_product->getProduct($product_id);

		if ($product_info) {
			if (isset($this->request->post['quantity'])) {
				$quantity = (int)$this->request->post['quantity'];
			} else {
				$quantity = 1;
			}

			if (isset($this->request->post['option'])) {
				$option = array_filter($this->request->post['option']);
			} else {
				$option = array();
			}

			$product_options = $this->model_catalog_product->getProductOptions($this->request->post['product_id']);

			foreach ($product_options as $product_option) {
				if ($product_option['required'] && empty($option[$product_option['product_option_id']])) {
					$json['error']['option'][$product_option['product_option_id']] = sprintf($this->language->get('error_required'), $product_option['name']);
				}
			}

			if (isset($this->request->post['recurring_id'])) {
				$recurring_id = $this->request->post['recurring_id'];
			} else {
				$recurring_id = 0;
			}

			$recurrings = $this->model_catalog_product->getProfiles($product_info['product_id']);

			if ($recurrings) {
				$recurring_ids = array();

				foreach ($recurrings as $recurring) {
					$recurring_ids[] = $recurring['recurring_id'];
				}

				if (!in_array($recurring_id, $recurring_ids)) {
					$json['error']['recurring'] = $this->language->get('error_recurring_required');
				}
			}



    
        if ($this->config->get('module_marketplace_status') && !isset($json['error']['option'])) {
          if (isset($this->session->data['error_order_limit']) && $this->session->data['error_order_limit']) {
            $json['success']  = $this->session->data['error_order_limit'];
            $json['error']    = $this->session->data['common_cart_total'];
          }
       }

      
			if (!$json) {
				$this->cart->add($this->request->post['product_id'], $quantity, $option, $recurring_id);

				$json['success'] = sprintf($this->language->get('text_success'), $this->url->link('product/product', 'product_id=' . $this->request->post['product_id']), $product_info['name'], $this->url->link('checkout/cart'));

				// Unset all shipping and payment methods
				unset($this->session->data['shipping_method']);
				unset($this->session->data['shipping_methods']);
				unset($this->session->data['payment_method']);
				unset($this->session->data['payment_methods']);

				// Totals
				$this->load->model('setting/extension');

				$totals = array();
				$taxes = $this->cart->getTaxes();
				$total = 0;
		
				// Because __call can not keep var references so we put them into an array. 			
				$total_data = array(
					'totals' => &$totals,
					'taxes'  => &$taxes,
					'total'  => &$total
				);

				// Display prices
				if ($this->customer->isLogged() || !$this->config->get('config_customer_price')) {
					$sort_order = array();

					$results = $this->model_setting_extension->getExtensions('total');

					foreach ($results as $key => $value) {
						$sort_order[$key] = $this->config->get('total_' . $value['code'] . '_sort_order');
					}

					array_multisort($sort_order, SORT_ASC, $results);

					foreach ($results as $result) {
						if ($this->config->get('total_' . $result['code'] . '_status')) {
							$this->load->model('extension/total/' . $result['code']);

							// We have to put the totals in an array so that they pass by reference.
							$this->{'model_extension_total_' . $result['code']}->getTotal($total_data);
						}
					}

					$sort_order = array();

					foreach ($totals as $key => $value) {
						$sort_order[$key] = $value['sort_order'];
					}

					array_multisort($sort_order, SORT_ASC, $totals);
				}

				$json['total'] = sprintf($this->language->get('text_items'), $this->cart->countProducts() + (isset($this->session->data['vouchers']) ? count($this->session->data['vouchers']) : 0), $this->currency->format($total, $this->session->data['currency']));
			} else {
				$json['redirect'] = str_replace('&amp;', '&', $this->url->link('product/product', 'product_id=' . $this->request->post['product_id']));
			}
		}


        if ($this->config->get('module_marketplace_status') ){
          $isAdded = $this->chk($product_id);
          
          if($isAdded['success']) {			
            unset($json['redirect']);			  
          }
        }


        if ($this->config->get('module_marketplace_status')) {
    			 if (isset($this->session->data['error_order_limit']) && $this->session->data['error_order_limit']) {
             $json['success'] = $this->session->data['error_order_limit'];
    				 $json['total']    = $this->session->data['common_cart_total'];
    			  	unset($json['redirect']);
    					unset($this->session->data['common_cart_total']);
    					unset($this->session->data['error_order_limit']);
    			 }
    		}

      

		$this->response->addHeader('Content-Type: application/json');
		$this->response->setOutput(json_encode($json));
	}

	public function edit() {
		$this->load->language('checkout/cart');

		$json = array();

		// Update
		if (!empty($this->request->post['quantity'])) {

            // gift code
            if ($this->config->get('module_marketplace_status') && $this->config->get('module_wk_crosssell_crosssell_status')) {
            foreach ($this->request->post['quantity'] as $post_key => $post_quantity) {
              if (isset($this->session->data['gifts'][$post_key])) {
                $total_quantity = $this->session->data['gifts'][$post_key] + $post_quantity;
                $this->request->post['quantity'][$post_key] = $total_quantity;
              }
            }
            }
            // gift code ends here
            
			foreach ($this->request->post['quantity'] as $key => $value) {
				$this->cart->update($key, $value);
			}

			$this->session->data['success'] = $this->language->get('text_remove');

			unset($this->session->data['shipping_method']);
			unset($this->session->data['shipping_methods']);
			unset($this->session->data['payment_method']);
			unset($this->session->data['payment_methods']);
			unset($this->session->data['reward']);

			$this->response->redirect($this->url->link('checkout/cart'));
		}

		$this->response->addHeader('Content-Type: application/json');
		$this->response->setOutput(json_encode($json));
	}

